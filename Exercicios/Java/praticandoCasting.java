/* Praticando Casting: atribuir o valor de um tipo de variavel a outro tipo de variavel */

package Java;

public class praticandoCasting {
	
	public static void main(String[] args) {
	
		int a, b;
		double resultado;
		
		a = 5;
		b = 2;
		
		resultado = (double) a / b;
		
		System.out.println(resultado);
	}
	

}
